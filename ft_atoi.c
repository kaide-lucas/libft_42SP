#include "libft.h"

int	ft_atoi(const char *n)
{
	int		res;
	int		signal;
	int		i;

	i = 0;
	res = 0;
	signal = 1;
	while ((n[i] >= 9 && n[i] <= 13) || (n[i] == ' '))
		i++;
	if (n[i] == '-' || n[i] == '+')
	{
		if (n[i] == '-')
			signal = -1;
			i++;
	}
	while (n[i] >= '0' && n[i] <= '9')
	{
		res = res * 10 + (n[i] - '0');
		i++;
	}
	return (signal * res);
}
