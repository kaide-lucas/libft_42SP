#include "libft.h"

int	ft_atoi(const char *n)
{
	int		res;
	int		signal;

	res = 0;
	signal = 1;
	while ((*n >= 9 && *n <= 13) || *n == 32)
		n++;
	if (*n == '-' || *n == '+')
	{
		if (*n == '-')
			signal *= -1;
		n++;
	}
	while (*n >= '0' && *n <= '9')
	{
		res = res * 10 + (*n - '0');
		n++;
	}
	return (res * signal);
}
